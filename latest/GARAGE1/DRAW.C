void Set_Video_Mode(int mode) //设置显示模式的函数
 {
	 union REGS inregs ,outregs; //定义输入和返回寄存器
	 inregs.h.ah=0; //ah 存放功能号0h 表示进入视频模式设定
     inregs.h.al=(unsigned char) mode ;    //al存放要设定的视频模式
	 int86(0x10,&inregs,&outregs);     //通过10h 中断设定显示模式
 }
